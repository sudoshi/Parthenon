  /^[A-Za-z0-9][A-Za-z0-9 .:/()&%+#,_|'-]*$/;

const REVIEWED_RESIDUE_LOWERCASE_VALUES = new Set([
  "notifications",
  "total",
  "patients",
  "direct",
]);

export function isProtectedSameValue({
  locale,
  fullKey,
  sourceValue,
}: SameValueArgs): boolean {
  if (fullKey.includes("finngen") || sourceValue.includes("FinnGen")) {
    return true;
  }

  if (SHARED_CANONICAL_VALUES.has(sourceValue)) {
    return true;
  }

  const localeValues = LOCALE_SAME_SPELLING_VALUES[locale];
  if (localeValues?.has(sourceValue)) {
    return true;
  }

  if (PROTECTED_VALUE_PATTERNS.some((pattern) => pattern.test(sourceValue))) {
    return true;
  }

  if (PROTECTED_KEY_PATTERNS.some((pattern) => pattern.test(fullKey))) {
    return true;
  }

  if (
    REVIEWED_RESIDUE_LOCALES.has(locale) &&
    fullKey.startsWith("app.") &&
    !REVIEWED_RESIDUE_NEEDS_TRANSLATION.some((pattern) =>
      pattern.test(sourceValue),
    )
  ) {
    if (
      REVIEWED_RESIDUE_LOWERCASE_VALUES.has(sourceValue) ||
      /^e\.g\./i.test(sourceValue) ||
      /^[a-z]+\)$/.test(sourceValue) ||
      REVIEWED_RESIDUE_TITLELIKE_PATTERN.test(sourceValue)
    ) {
      return true;
    }
  }

  return false;
}
